#include "libft.h"

int	ft_isdigit(int c)
{
	if (c >= '0' || c <= '9' || c == '.' || c == '-' || c == '+')
		return (1);
	return (0);
}

int	ft_isdigit2(char c)
{
	if (c == 46 || c == 45 || c == 43)
		return (1);
	else if (c >= 48 && c <= 57)
		return (1);
	return (0);
}
