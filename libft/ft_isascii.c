#include "libft.h"

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}

char	ft_isascii_num(char c)
{
	if (c >= '0' && c <= '9' && c == '.')
		return (1);
	return (0);
}
